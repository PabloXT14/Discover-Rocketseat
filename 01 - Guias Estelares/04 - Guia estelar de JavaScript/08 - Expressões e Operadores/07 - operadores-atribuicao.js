/*
    Operadores de Atribuição (Assigment)
*/

// Assigment
x = 1

// Addition Assigment 
x += 2 // x = x + 2

// Subtraction Assigment
x -= 1 // x = x - 1

// Multiplication Assigment
x *= 2 // x = x * 2

// Division Assigment
x /= 2 // x = x / 2

// Remaider Assigment
x %= 1.5 // x = x % 2

// Exponetiation Assigment
x **= 2 // x = x**2


